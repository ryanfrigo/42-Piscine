#include "ft_bsq.h"

int		is_space(char c)
{
	return (c == ' ' || c == '\t' || c == '\n'
		|| c == '\v' || c == '\f' || c == '\r');
}

int		ft_atoi(char *str)
{
	int i;
	int sum;

	sum = 0;
	i = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		sum *= 10;
		sum += str[i] - '0';
		i++;
	}
	return (sum);
}
