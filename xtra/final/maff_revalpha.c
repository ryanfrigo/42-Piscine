#include <unistd.h>

int		main(void)
{
	int i = 122;
	int c;
	while (i >= 97)
	{
		if (i % 2 == 0)
			write(1, &i, 1);
		else
		{
			c = i - 32;
			write(1, &c, 1);
		}
		i--;
	}
	return (0);
}
