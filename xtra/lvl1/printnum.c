#include <unistd.h>

void	ft_print_numbers(void)
{
	char s;
	char e;

	s = '0';
	e = '9';
	while (s <= e)
	{
		write(1, &s, 1);
		s++;
	}
}

int		main(void)
{
	ft_print_numbers();
	return (0);
}

